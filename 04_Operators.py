'''
Operators :
Addition: "+"
Subtraction: "-"
Multiplication: "*"
Division: "/"
Exponential: "**"
Modulus: "%"
Floor Division: "//"
'''
a = 38
b = 10
print('Addition: ',a + b,'\n') # adds
print('Subtraction: ',a - b,'\n') # subtacts
print('Multiplication: ',a * b,'\n') # multiplies
print('Division: ',a / b,'\n') # divides and give answer in form of decimal
print('Exponential: ',a // b,'\n') # Gives only quotient
print('Modulus: ',a % b,'\n') # Gives only remainder
print('Floor Division: ',a ** b,'\n') #  Similar to a^b